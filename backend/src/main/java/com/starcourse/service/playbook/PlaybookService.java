    private static void add(String dimensionId, String subject, String title, String content, List<String> tags) {
        ALL_MATERIALS.add(new PlaybookMaterial(dimensionId, subject, title, content, tags));
    }

    public List<PlaybookMaterial> getRelevantMaterials(TeacherProfile profile) {
        var matchedDimensions = PlaybookRegistry.matchDimensions(profile);
        var matchedIds = matchedDimensions.stream()
                .map(PlaybookRegistry.Dimension::id)
                .collect(Collectors.toSet());

        return ALL_MATERIALS.stream()
                .filter(m -> matchedIds.contains(m.dimensionId()))
                .sorted(Comparator.comparing(PlaybookMaterial::dimensionId))
                .toList();
    }

    public List<PlaybookMaterial> getAllMaterials() {
        return Collections.unmodifiableList(ALL_MATERIALS);
    }
